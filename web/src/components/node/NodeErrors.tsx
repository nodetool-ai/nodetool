const GITHUB_ISSUE_URL =
  "https://github.com/nodetool-ai/nodetool/issues/new";

const REPORT_LINES = 100;

/** Build a pre-filled GitHub new-issue URL. */
function buildReportUrl(
  nodeType: string,
  errorText: string,
  logLines: string[],
  systemInfo: string
): string {
  const title = `[Bug] Node "${nodeType}" error: ${errorText.split("\n")[0].slice(0, 80)}`;

  const lastLogs =
    logLines.length > 0
      ? logLines.slice(-REPORT_LINES).join("\n")
      : "(no logs captured)";

  const body = `## Node Error Report

**Node type:** \`${nodeType}\`
**NodeTool version:** ${VERSION}

### Error

\`\`\`
${errorText}
\`\`\`

### System Information

\`\`\`
${systemInfo}
\`\`\`

### Last ${REPORT_LINES} log lines

\`\`\`
${lastLogs}
\`\`\`

---
*Automatically generated by the "Report this issue" button in NodeTool.*
`;

  const params = new URLSearchParams({
    title,
    body,
    labels: "bug",
  });

  return `${GITHUB_ISSUE_URL}?${params.toString()}`;
}

/** Gather browser / OS system information. */
function getSystemInfo(): string {
  const lines: string[] = [
    `Browser: ${navigator.userAgent}`,
    `Platform: ${navigator.platform}`,
    `Language: ${navigator.language}`,
    `Screen: ${window.screen.width}x${window.screen.height} (devicePixelRatio: ${window.devicePixelRatio})`,
    `NodeTool version: ${VERSION}`,
  ];
  return lines.join("\n");
}

