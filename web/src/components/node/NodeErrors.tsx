export const NodeErrors: React.FC<{
  id: string;
  workflow_id: string;
  nodeType?: string;
}> = ({ id, workflow_id, nodeType = "unknown" }) => {
  const theme = useTheme();
  const error = useErrorStore((state) =>
    workflow_id !== undefined ? state.getError(workflow_id, id) : undefined
  );

  const logs = useLogsStore(
    (state) => state.logsByNode[nodeLogKey(workflow_id, id)]
  );

  const handleReport = useCallback(() => {
    const errorText = nodeErrorToDisplayString(error);
    const logLines = (logs ?? []).map(
      (l) => `[${l.severity.toUpperCase()}] ${l.content}`
    );
    const url = buildReportUrl(
      nodeType,
      errorText,
      logLines,
      getSystemInfo()
    );
    window.open(url, "_blank", "noopener,noreferrer");
  }, [error, logs, nodeType]);

  if (!hasNodeError(error)) {
    return null;
  }

  const errorDisplay = nodeErrorToDisplayString(error);

  return (
    <div css={errorStyles(theme)} className="node-error nodrag nowheel">
      <div className="error-actions">
        <Tooltip title="Report this issue on GitHub">
          <button
            className="report-button nodrag"
            onClick={handleReport}
            tabIndex={-1}
            aria-label="Report this issue on GitHub"
          >
            <BugReportIcon sx={{ fontSize: "0.9em" }} />
            Report
          </button>
        </Tooltip>
        <CopyButton
          value={errorDisplay}
          tooltip="Copy to clipboard"
          tabIndex={-1}
        />
      </div>
      <div className="error-text">{errorDisplay}</div>
    </div>
  );
};

export default memo(NodeErrors, isEqual);